// 根据分数和用户信息生成评语
export function generateCommentaryFull(
  pathway: Pathway,
  userName: string = 'Seeker'
): string {
  const templates = commentaryTemplatesFull[pathway.id]
  if (!templates || templates.length === 0) {
    // 如果没有找到对应模板，返回通用评语
    return `${userName}，你是${pathway.name}途径的非凡者。${pathway.description}`
  }
  // 随机选择一个模板
  const template = templates[Math.floor(Math.random() * templates.length)]
  return template.replace(/{name}/g, userName)
}

// 根据用户特质生成个性化格言
export function generateCustomMottoFull(
  pathway: Pathway,
  _traits: string[]
): string {
  const defaultMottos = pathway.motto
  return defaultMottos[Math.floor(Math.random() * defaultMottos.length)]
}
